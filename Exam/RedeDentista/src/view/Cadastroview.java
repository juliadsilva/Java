    private void limparCampos() {
        tf_Nome.setText("");
        tf_Telefone.setText("");
        tf_Documento.setText("");
        tf_CRO.setText("");
        tf_Avaliacao.setText("");
        bg_Especialidade.clearSelection();
    }
    
    private void ler(){
        dtm.setRowCount(0);
        for (Dentista dentista : ArquivoController.listarDentistas()) {
            dtm.addRow(new Object[]{
                dentista.getNome(), dentista.getDocumento(), dentista.getCro(), dentista.getTelefone(), dentista.getEspecialidade(), dentista.getAvaliacao()
            });
        };
        numero.setText(Integer.toString(dtm.getRowCount()));
    }
    
