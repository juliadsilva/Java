    private void btn_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CadastrarActionPerformed
        String nome = tf_Nome.getText();
        String documento = tf_Documento.getText();
        String cro = tf_CRO.getText();
        long telefone = Long.parseLong(tf_Telefone.getText());
        int avaliacao = Integer.parseInt(tf_Avaliacao.getText());
        
        String especialidade = null;
        if(rb_Clinica.isSelected()){
            especialidade = "Clinica Geral";
        }else if(rb_Cirurgia.isSelected()){
            especialidade = "Cirurgia";
        }else if(rb_Dentistica.isSelected()){
            especialidade = "Dentistica";
        }
        
        try {
            DentistaController.salvarDentista(nome, documento, cro, telefone, especialidade, avaliacao);
            JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso", "Rede de Dentistas", JOptionPane.INFORMATION_MESSAGE);
            limparCampos();
        }catch (NomeVazioException e) {
            JOptionPane.showMessageDialog(this, "Nome", "Campo Vazio", JOptionPane.ERROR_MESSAGE);
        }catch (DocumentoVazioException e){
            JOptionPane.showMessageDialog(this, "Documento", "Campo Vazio", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_CadastrarActionPerformed

