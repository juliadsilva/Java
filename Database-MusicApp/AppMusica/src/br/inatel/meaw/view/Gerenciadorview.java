    private void tabelaUsuarios(){
        dtmU.setRowCount(0);
 
        for (Usuario usuario : gerenciadorDAO.listarUsuarios(mySQLConnection)){
            dtmU.addRow(new Object[]{
                usuario.getIdUsuario(), usuario.getNomeUsuario(), usuario.getEmail(), usuario.getIdTipoUsuario()
            });
        }
    }
    
    private void tabelaMusica(){
        dtmM.setRowCount(0);
                
        for (Musica musica : gerenciadorDAO.buscarMusicas(mySQLConnection)) {        
            dtmM.addRow(new Object[]{
                musica.getIdMusica(), musica.getNomeMusica(), musica.getnomeArtista(), musica.getGenero(), musica.getTempoDuracao(), musica.getAno()
            });
        }
    }

    
