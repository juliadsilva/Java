    private void btnExcluirMusicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirMusicaActionPerformed
        try{
            int idMusica = Integer.parseInt(excluiridMusica.getText());
            artistaDAO.descQuantidadeMusica(mySQLConnection, idMusica);
            gerenciadorDAO.deletarMusica(mySQLConnection, idMusica);
            limparCampos();
            tabelaMusica();
        } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Insira um id valido!", "ERRO!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirMusicaActionPerformed

    private void btnCadastrarMusicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarMusicaActionPerformed
        try{
           music.setNomeMusica(emusicaNome.getText());
           music.setTempoDuracao(Double.parseDouble(emusicaTempo.getText()));
           music.setAno(Integer.parseInt(emusicaAno.getText()));
        
           setGeneroMusica(music);
        
           String auxArtistaNome = (String) comboboxArtista.getSelectedItem();
           music.setnomeArtista(auxArtistaNome);
                
           gerenciadorDAO.insertMusica(mySQLConnection, music);
           artistaDAO.inserirQuantidadeMusica(mySQLConnection, auxArtistaNome);
           limparCampos();
           tabelaMusica();
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Os campos Tempo de Duração e Ano devem ser preenchidos com valores numéricos.", "ERRO!", JOptionPane.ERROR_MESSAGE);
        }catch(CampoVazioException e){
            JOptionPane.showMessageDialog(this, "Preencha todo os campos.", "ERRO!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarMusicaActionPerformed

