    private void btnCadastrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarUsuarioActionPerformed
        
        String tipo =  null;
        
        if(radioGerenciador.isSelected()){
            user = new Gerenciador();
            userDAO = new GerenciadorDAO();            
        }else if(radioUsuario.isSelected()){
            user = new UserComum();
            userDAO = new UserComumDAO();
        }else if(radioPremium.isSelected()){
            user = new UserPremium();
            userDAO = new UserPremiumDAO();
        }else{
            JOptionPane.showMessageDialog(this, "Selecione o tipo.","Cadastro", JOptionPane.ERROR_MESSAGE);
        }
        
        try{
            user.setNomeUsuario(entradanomeUsuario.getText());
            user.setEmail(entradaemailUsuario.getText());
            user.setSenha(entradasenhaUsuario.getText());
            userDAO.existeUsuario(mySQLConnection, user);
            userDAO.inserirUsuario(mySQLConnection, user);
            limparCampos();
        }catch (EmailInvalidoException e){
            JOptionPane.showMessageDialog(this, "E-mail invalido", tipo, JOptionPane.ERROR_MESSAGE); 
        }catch(CampoVazioException e){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos", "Cadastro", JOptionPane.ERROR_MESSAGE);
        }catch (UsuarioExistenteException e){
            JOptionPane.showMessageDialog(this, "E-mail de usuario j√° existente", "Cadastro", JOptionPane.ERROR_MESSAGE); 
        }   
    }//GEN-LAST:event_btnCadastrarUsuarioActionPerformed

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        Usuario userLogin = new Usuario();
        
        String email = loginemailUsuario.getText();
        String senha = loginsenhaUsuario.getText();
        
        userLogin = usuarioAdd.buscarUsuario(mySQLConnection, email, senha);
        int id = userLogin.getIdUsuario();
        Constants.login = id;
        
        if(id!= 0){
            int tipo = userLogin.getIdTipoUsuario();
            switch(tipo){
                case 101: 
                    Gerenciadorview gerenciadorView = new Gerenciadorview();
                    this.dispose();
                    gerenciadorView.setVisible(true); 
                    break;
                case 202:
                    Musicaview musicaView = new Musicaview();
                    this.dispose();
                    musicaView.setVisible(true);
                    break;
                case 303:
                    Playlistview playlistView = new Playlistview();
                    this.dispose();
                    playlistView.setVisible(true);
                    break;
            }
            limparCampos();            
        }
        else JOptionPane.showMessageDialog(this, "Invalido", "Login", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnLoginActionPerformed

